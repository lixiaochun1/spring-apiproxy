package name.skitazaki.apiproxy.model;

public class Configuration {

	private String name;
	private String server;

	public String getName() {
		return name;
	}

	public String getServer() {
		return server;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setServer(String server) {
		this.server = server;
	}
}
